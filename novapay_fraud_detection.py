from sklearn.preprocessing import StandardScaler, LabelEncoder
from imblearn.over_sampling import SMOTE
def prepare_model_data(df, target_col='is_fraud', time_col='timestamp', train_ratio=0.8):

    df[time_col] = pd.to_datetime(df[time_col])
    df = df.sort_values(time_col)

    feature_cols = [
        'normalized_behavioral_risk',
        'high_txn_velocity_1h', 'high_txn_velocity_24h',
        'velocity_24h_amount_interaction', 'ip_device_interaction',
        'customer_txn_count', 'customer_total_amount', 'customer_corridor_diversity',
        'customer_avg_amount', 'chargeback_amount_interaction', 'txn_ratio_1h_24h',
        'new_corridor_flag', 'device_count_per_customer', 'high_ip_risk',
        'new_account', 'high_risk_score', 'high_risk_hour'
    ]

    available_features = [f for f in feature_cols if f in df.columns]

    # Time-based split
    cutoff = int(len(df) * train_ratio)
    train_df = df.iloc[:cutoff]
    test_df = df.iloc[cutoff:]

    X_train = train_df[available_features].copy()
    y_train = train_df[target_col].copy()
    X_test = test_df[available_features].copy()
    y_test = test_df[target_col].copy()

    # Encoding
    categorical_cols = [
        'new_corridor_flag', 'high_txn_velocity_1h', 'high_txn_velocity_24h',
        'high_ip_risk', 'new_account', 'high_risk_score', 'high_risk_hour'
    ]

    encoders = {}
    for col in categorical_cols:
        if col in X_train.columns:
            le = LabelEncoder()
            X_train[col] = le.fit_transform(X_train[col].astype(str))
            X_test[col] = le.transform(X_test[col].astype(str))
            encoders[col] = le

    # Scaling
    numeric_cols = [
        'customer_txn_count', 'customer_total_amount', 'customer_corridor_diversity',
        'customer_avg_amount', 'device_count_per_customer',
        'velocity_24h_amount_interaction', 'txn_ratio_1h_24h',
        'chargeback_amount_interaction', 'normalized_behavioral_risk'
    ]
    numeric_cols = [c for c in numeric_cols if c in X_train.columns]

    scaler = StandardScaler()
    X_train[numeric_cols] = scaler.fit_transform(X_train[numeric_cols])
    X_test[numeric_cols] = scaler.transform(X_test[numeric_cols])

    # SMOTE (for comparison later)
    smote = SMOTE(random_state=42)
    X_train_bal, y_train_bal = smote.fit_resample(X_train, y_train)

    print("Original train:", X_train.shape, "Fraud ratio:", y_train.mean())
    print("SMOTE train:", X_train_bal.shape, "Fraud ratio:", y_train_bal.mean())

    return X_train, X_train_bal, X_test, y_train, y_train_bal, y_test, scaler, encoders, available_features

