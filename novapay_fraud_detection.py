from sklearn.metrics import precision_recall_curve, f1_score

def find_best_threshold(model, X_test, y_test, positive_label=1):
    # Get predicted probabilities for the positive class
    y_prob = model.predict_proba(X_test)[:, 1]

    # Get precision, recall, thresholds
    precision, recall, thresholds = precision_recall_curve(y_test, y_prob, pos_label=positive_label)

    # Compute F1 for each threshold
    f1_scores = 2 * (precision * recall) / (precision + recall + 1e-6)

    # Best threshold based on max F1
    best_idx = f1_scores.argmax()
    best_threshold = thresholds[best_idx]
    best_f1 = f1_scores[best_idx]
    best_precision = precision[best_idx]
    best_recall = recall[best_idx]

    print(f"Best threshold for {model.__class__.__name__}: {best_threshold:.3f}")
    print(f"Precision: {best_precision:.3f}, Recall: {best_recall:.3f}, F1: {best_f1:.3f}")

    return best_threshold, best_precision, best_recall, best_f1

