"""### Generate Review Template Using Test Data"""

def generate_review_template(idx, model, explainer, X_test, y_test, feature_names, threshold=0.5):

    # Prediction
    pred_proba = model.predict_proba(X_test.iloc[idx:idx+1])[0, 1]
    prediction = "FRAUD" if pred_proba >= threshold else "LEGITIMATE"
    true_label = "FRAUD" if y_test.iloc[idx] == 1 else "LEGITIMATE"

    # Risk level
    if pred_proba >= 0.8:
        risk_level = "HIGH"
    elif pred_proba >= 0.5:
        risk_level = "MEDIUM"
    else:
        risk_level = "LOW"

    # Compute SHAP safely
    shap_vals = explainer.shap_values(X_test.iloc[idx:idx+1])

    # Handle LightGBM SHAP output format
    if isinstance(shap_vals, list):
        shap_vals = shap_vals[1][0]   # binary classification
    else:
        shap_vals = shap_vals[0]

    feature_vals = X_test.iloc[idx].values

    contributions = list(zip(feature_names, feature_vals, shap_vals))
    contributions.sort(key=lambda x: abs(x[2]), reverse=True)

    # Top drivers
    risk_factors = [c for c in contributions if c[2] > 0][:5]
    protective_factors = [c for c in contributions if c[2] < 0][:5]

    # Reason codes
    reason_codes = [
        reason_code_map.get(feat, feat)
        for feat, _, _ in risk_factors
    ]

    # Build report
    report = f"""
===============================
 FRAUD REVIEW TEMPLATE
===============================

Transaction ID: {idx}

Prediction: {prediction}
Confidence Score: {pred_proba:.2%}
Risk Level: {risk_level}

True Label: {true_label}

-------------------------------
 REASON CODES (Why flagged)
-------------------------------
"""

    for r in reason_codes:
        report += f" - {r}\n"

    report += "\n-------------------------------\n TOP RISK DRIVERS\n-------------------------------\n"

    for feat, val, shap_val in risk_factors:
        report += f" - {feat}: {val:.4f} (Impact: +{shap_val:.4f})\n"

    report += "\n-------------------------------\n PROTECTIVE FACTORS\n-------------------------------\n"

    for feat, val, shap_val in protective_factors:
        report += f" - {feat}: {val:.4f} (Impact: {shap_val:.4f})\n"

    return report

