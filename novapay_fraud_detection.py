"""####HANDLE MISSING VALUES

#### For 'amount_usd'
The process involved:
* Estimate the missing amount_usd using the relationship between amount_src and amount_usd.

* Then calculate average exchange rates per source_currency using transactions where amount_usd is already available (amount_usd / amount_src).

* Store these rates in a lookup dictionary for efficient reference during imputation.
"""

df["source_currency"].value_counts()   # Check source currency counts

# Select rows where amount_usd is present
df_with_usd = df[df['amount_usd'].notna()]

# Compute average exchange rate per source currency
# (mean of amount_usd / amount_src)
exchange_rate_lookup = df_with_usd.groupby('source_currency').apply(
    lambda x: (x['amount_usd'] / x['amount_src']).mean()
).to_dict()

print("Currency conversion lookup:", exchange_rate_lookup)

"""#####To fill missing values
* Impute missing amount_usd by multiplying amount_src with the corresponding currency exchange rate.

* Use a default rate of 1 if a currency is not found in the lookup.

* Preserve existing amount_usd values to maintain data integrity.
"""

# Fill missing amount_usd using the lookup, defaulting to 1 if currency not found
def fill_amount_usd(row):
    if pd.notna(row['amount_usd']):
        return row['amount_usd']
    else:
        rate = exchange_rate_lookup.get(row['source_currency'], 1)
        return row['amount_src'] * rate

df['amount_usd'] = df.apply(fill_amount_usd, axis=1)

